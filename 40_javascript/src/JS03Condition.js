// 条件控制示例

// if...else 示例
let age = 18;
if (age >= 18) {
    console.log("你是成年人");
} else {
    console.log("你是未成年人");
}

// switch 示例
let day = 3;
switch (day) {
    case 1:
        console.log("今天是星期一");
        break;
    case 2:
        console.log("今天是星期二");
        break;
    case 3:
        console.log("今天是星期三");
        break;
    default:
        console.log("未知的日期");
        break;
}

// 三元运算符示例
let score = 85;
let result = score >= 60 ? "及格" : "不及格";
console.log(`考试结果: ${result}`);